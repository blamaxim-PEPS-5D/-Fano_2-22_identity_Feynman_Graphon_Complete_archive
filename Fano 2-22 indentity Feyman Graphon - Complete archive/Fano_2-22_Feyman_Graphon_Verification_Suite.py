def fibonacci_sphere_3d(n):
    indices = np.arange(n) + 0.5
    phi = np.pi * (1 + np.sqrt(5)) * indices
    y = 1 - 2 * indices / n
    radius = np.sqrt(1 - y**2)
    x = np.cos(phi) * radius
    z = np.sin(phi) * radius
    return np.vstack([x, y, z]).T

def fibonacci_sphere_4d(n):
    pts3d = fibonacci_sphere_3d(n)
    psi = np.arccos(2 * np.random.rand(n) - 1)
    w = np.cos(psi)
    sin_psi = np.sin(psi)
    x = sin_psi * pts3d[:, 0]
    y = sin_psi * pts3d[:, 1]
    z = sin_psi * pts3d[:, 2]
    return np.vstack([x, y, z, w]).T

def distance_on_S3(p, q):
    dot = np.clip(np.dot(p, q), -1, 1)
    return PI * np.arccos(dot)

def compute_W_Gk(points, k):
    n = len(points)
    dist_matrix = cdist(points, points, metric=lambda u, v: distance_on_S3(u, v))
    W_Gk = np.exp(-gamma * dist_matrix / (2**k))
    W_cont = kappa * VEV_sq * np.exp(-gamma * dist_matrix)
    return W_Gk, W_cont

def cut_norm_for_subset(S, T, W_Gk, W_cont):
    n = len(W_Gk)
    return (np.sum(W_Gk[S][:, T]) - np.sum(W_cont[S][:, T])) / (n * n)

def estimate_cut_norm_optimized(points, W_Gk, W_cont, n_mc=5000, n_opt_iter=20):
    n = len(points)
    best_value = 0.0
    best_S = np.zeros(n, dtype=bool)
    best_T = np.zeros(n, dtype=bool)

    for _ in range(n_mc):
        S = np.random.rand(n) < 0.5
        T = np.random.rand(n) < 0.5
        if not np.any(S) or not np.any(T):
            continue
        val = abs(cut_norm_for_subset(S, T, W_Gk, W_cont))
        if val > best_value:
            best_value = val
            best_S, best_T = S.copy(), T.copy()

    for _ in range(n_opt_iter):
        improved = False
        for i in range(n):
            S_test = best_S.copy()
            S_test[i] = not S_test[i]
            if np.any(S_test):
                val = abs(cut_norm_for_subset(S_test, best_T, W_Gk, W_cont))
                if val > best_value:
                    best_value = val
                    best_S = S_test.copy()
                    improved = True
            T_test = best_T.copy()
            T_test[i] = not T_test[i]
            if np.any(T_test):
                val = abs(cut_norm_for_subset(best_S, T_test, W_Gk, W_cont))
                if val > best_value:
                    best_value = val
                    best_T = T_test.copy()
                    improved = True
        if not improved:
            break
    return best_value

