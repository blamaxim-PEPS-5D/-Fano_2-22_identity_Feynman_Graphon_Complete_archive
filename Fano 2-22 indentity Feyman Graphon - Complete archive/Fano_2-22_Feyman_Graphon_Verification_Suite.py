PI = np.pi
R = PI
DS = 2*np.sqrt(2) - np.sqrt(7)
dth = (1/(2*PI)) * np.log(np.sqrt(8)/(np.sqrt(8)-np.sqrt(7)))
VEV_sq = PI**4 + 1
VEV = np.sqrt(VEV_sq)
gamma = VEV * (24/25) * (dth / DS)
kappa = (24/25) * (dth / DS)

